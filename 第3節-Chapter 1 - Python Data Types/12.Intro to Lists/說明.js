// ❗ List 像是 Array
//  寫法 [12, "Hello", True]
//  List is an ordered(順序性) sequence(連續性) of data
//  通常有關連性的data的時候會使用 list
//  很常會使用 List of Lists (矩陣)
//
// 1. len() - 長度
// 2. Indexing rule (有位置數字的概念)
// 3. slicing rule (一樣可以像 string 切到想要的數字範圍)
// 4. count() - 這個 list 某個 element(元素) 出現的次數
// 5. index() - 該 element 第一次出現的位置
// 6. 但 list 沒有 find()
// 7. set() 是種 typecasting 可以將 list into(換成) set
// 8. list 也可以使用 + 來 concatenation(串接)
// 9. list 也可以與數字 相乘
// 10. list is mutable(可以改變的)(Remember, strings are immutable)